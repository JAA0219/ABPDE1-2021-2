    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        final String SQL_INSERT = "INSERT INTO Usuario(Nombre,Apellido,Cedula,Tipo_Examen,Fecha)"
                + "VALUES( ?,?,?,?,? );";
        
        try {
            // TODO add your handling code here:
            
            
            Conector objCone = new Conector();
            Connection cn = objCone.getConexion();
            PreparedStatement consulta = cn.prepareStatement(SQL_INSERT);
            
            consulta.setString(1,txtNombre.getText());
            consulta.setString(2,txtApellido.getText());
            consulta.setString(3,txtCedula.getText());
            consulta.setString(4,txtTExamen.getText());
            consulta.setString(5,txtFecha.getText());
            
            consulta.executeUpdate();
            
            txtNombre.setText("");
            txtApellido.setText("");
            txtCedula.setText("");
            txtTExamen.setText("");
            txtFecha.setText("");
            
            JOptionPane.showMessageDialog(null,"Se guardo con exito");
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Error al gurdar","ERROR!!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

