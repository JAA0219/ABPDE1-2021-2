    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String busqueda = "";
        String tipoExamen = "";
          System.out.print(tipoExamen);
        tipoExamen = ( btn1.isSelected() ) ? "examen lipidico" : tipoExamen;
        tipoExamen = ( btn2.isSelected() ) ? "examen renal" : tipoExamen;
        tipoExamen = ( btn3.isSelected() ) ? "examen parasito" : tipoExamen;
        tipoExamen = ( btn4.isSelected() ) ? "examen tirodeo" : tipoExamen;
        System.out.print(tipoExamen);
        
        if (!"".equals(txtBusqueda.getText())) {

            busqueda = "SELECT * FROM Usuario "
                    + "WHERE Tipo_examen='" + tipoExamen + "' AND Nombre='" + txtBusqueda.getText() + "';";;
        } else {

            busqueda = "SELECT * FROM Usuario "
                    + "WHERE Tipo_examen='" + tipoExamen + "';";
        }
      final String SQL_SELECT = busqueda;
        
        try {
           

            Conector ObjConec = new Conector();
            Connection cn = ObjConec.getConexion();
            PreparedStatement consulta = cn.prepareStatement(SQL_SELECT);
            ResultSet rs = consulta.executeQuery();

            while (rs.next()) {

                Object[] fila = new Object[5];

                fila[0] = rs.getString("Nombre");
                fila[1] = rs.getString("Apellido");
                fila[2] = rs.getString("Cedula");
                fila[3] = rs.getString("Tipo_examen");
                fila[4] = rs.getString("Fecha");
                
                modelo.addRow(fila);
            }
             tablaRegistro.setModel(modelo);
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Error al conectar con la base de datos","ERROR!",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

